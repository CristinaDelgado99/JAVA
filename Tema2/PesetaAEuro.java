public class PesetaAEuro{
	 
	public static void main(String[]args){
	
		int pesetas;
		pesetas = 1700;
		
		double euros;
		euros = (double) pesetas * 0.0060;
		
		System.out.printf("%d pesetas son %.2f euros", pesetas, euros);
	

	
	
		
	}

}

