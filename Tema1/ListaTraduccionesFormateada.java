public class ListaTraduccionesFormateada{

  public static void main (String[]args){
		
	System.out.println("\u001B[36mLISTA DE PALABRAS Y SU TRADUCCI\u00d3N AL INGL\u00c9S\u001B[37m");	
	System.out.printf("%-10s %-10s\n", "Happy", "Feliz");     
	System.out.printf("%-10s %-10s\n", "Sad", "Triste");
	System.out.printf("%-10s %-10s\n", "Stressed", "Estresado");
	System.out.printf("%-10s %-10s\n", "Intrigued", "Intrigado");
	System.out.printf("%-10s %-10s\n", "Amused", "Asombrado");
	System.out.printf("%-10s %-10s\n", "Angry", "Cabreado");
	System.out.printf("%-10s %-10s\n", "Excited", "Emocionado");
	System.out.printf("%-10s %-10s\n", "Hungry", "Hambriento");
	System.out.printf("%-10s %-10s\n", "Anxious", "Ansioso");
	System.out.printf("%-10s %-10s\n", "Depressed", "Deprimido");
		
			
  }
}

