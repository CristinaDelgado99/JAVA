public class EscenaCaracteres {

	public static void main(String[] args) {

		System.out.println("Nota: Estos símbolos: |\\ representan notas musicales.");
		System.out.println("                     @                                ");
		System.out.println("Desgraciadamente, no he sido capaz de añadir unicode porque mi consola no lo lee.\n");

		System.out.println("     ±         **                *");
		System.out.println("    / \\       ****     *        ***    ");
		System.out.println("   / o \\              ***                      * ");
		System.out.println("  /-----\\                    **               ***");
		System.out.println("  |# [] |                   **** ");
		System.out.println("----------------------------------|\\--------------------");
		System.out.println("    *  *            |\\  ___      @       °");
		System.out.println("     \\/  °         @   (^o^)               *  *");
		System.out.println("              °      --/|_|\\--        °     \\/");
		System.out.println("         *  *       /   / \\   \\");
		System.out.println("          \\/       /           \\       *  *");
		System.out.println("                  /_____________\\       \\/");
		System.out.println("-------------------------------------------------------");
	}

}
